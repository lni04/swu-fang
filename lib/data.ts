// 选课记录（内存存储）
export interface 选课记录 {
  选课ID: number
  学号: string
  排课ID: number
  状态: string
  操作时间: string
}

let 选课ID计数器 = 1
export const 选课列表: 选课记录[] = []

export function 添加选课(学号: string, 排课ID: number): 选课记录 {
  const 记录: 选课记录 = {
    选课ID: 选课ID计数器++,
    学号,
    排课ID,
    状态: "已选",
    操作时间: new Date().toLocaleString("zh-CN"),
  }
  选课列表.push(记录)
  return 记录
}

export function 退选课程(选课ID: number): boolean {
  const 记录 = 选课列表.find((x) => x.选课ID === 选课ID)
  if (记录) {
    记录.状态 = "已退"
    记录.操作时间 = new Date().toLocaleString("zh-CN")
    return true
  }
  return false
}

export function 获取学生选课(学号: string): 选课记录[] {
  return 选课列表.filter((x) => x.学号 === 学号 && x.状态 === "已选")
}

export function 检查是否已选(学号: string, 排课ID: number): boolean {
  return 选课列表.some((x) => x.学号 === 学号 && x.排课ID === 排课ID && x.状态 === "已选")
}

// 获取课程完整信息
export function 获取排课详情(排课ID: number) {
  const 排课 = 排课列表.find((p) => p.排课ID === 排课ID)
  if (!排课) return null
  const 课程 = 课程列表.find((c) => c.课程号 === 排课.课程号)
  const 教师 = 教师列表.find((t) => t.教师ID === 排课.教师ID)
  return { ...排课, ...课程, 教师姓名: 教师?.姓名 }
}
